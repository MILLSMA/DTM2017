<h2 class='content_title'>Vocational Pathways</h2>
<p>The NZ Government has developed a new education tool called Vocational Pathways.  Vocational Pathways provides a clear framework for vocational options and improves the links between education decisions and future employment. Using this tool, students are able to map and plan towards a clear career goal whether that is in the workforce or moving towards further study.  
</p><p>
The pathways help you as a student, see how your learning and achievement will be valued in the ‘real world’ when you look for a job and start your career.  
</p><p>
Achieving Vocational Pathways means that as a student you have developed skills and achieved in areas that employers’ value, and that you have skills and knowledge that is relevant to their industries.  
</p><p>
By achieving enough credits from the standards recommended by the sector, you can have your NCEA endorsed by one or many vocational pathways. This makes it easier for employers to see if you have the strengths and abilities they are looking for.  
</p><p>
If you are aiming for a career in a sector, the pathway helps you to see which subjects and standards you should do to get to where you want to go. For most jobs or careers, you’ll definitely need to further your education after school through tertiary study or training on the job.  
</p><p>
Profile Builder – Through your Vocational Pathways Profile you will be able to see a graph which can show you what you plan to learn or your current achievements.  The profile builder will be available via the Portal. 
</p><p>
The Job Profiles – Provides a large number of roles and occupations in each industry sector. If you haven’t decided and you are thinking about the future, the pathways can help see how your strengths and interests match up to an amazing range of jobs and study options out there.  
</p><p>
There are six pathways which represent new ways to structure and achieve NCEA.</p>
	<ul>
	<li>Creative Industries</li>
	<li>Primary Industries</li>
	<li>Service Industries</li>  
	<li>Social and Community Services</li>  
	<li>Manufacturing and Technology</li>  
	<li>Constructions and Infrastructure</li>  
	</ul>
