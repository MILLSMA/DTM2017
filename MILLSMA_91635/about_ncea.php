<h2 class='content_title'>National Certificates in Educational Achievement (NCEA) </h2>
<p>This information is a summary; full details are available through the school, on our website or at www.nzqa.govt.nz.</p>

	<h3>How will I be assessed? </h3>
<p>For assessment purposes, the skills and knowledge that you gain in subjects are described in detail by ‘standards’.  Standards come in a range of levels, reflecting how demanding they are.  Some standards are internally assessed at school, and some externally in end-of-year examinations.  Subjects are arranged in groups of standards, for example, a year long course worth 20 credits generally consists of 5-6 standards.  Most courses have 18-21 credits. </p>
	<h3>Standards </h3>
<p>Subjects are assessed using Unit Standards and Achievement Standards.  For Achievement Standards you can be graded: “Not Achieved”, “Achieved”, “Merit” or “Excellence”.  Unit Standards are generally graded “Not Achieved” or “Achieved”. </p> 
	<h3>Credits  </h3>
<p>Every standard achieved is worth a set number of credits.  A year long course is usually worth about 18-21 credits.  Credits are like points toward your qualification, and they mount up till you achieve national qualifications.  Note that credits can be gained over more than one year.  It is important to ensure that you gain the necessary standards for automatic entry to subjects you wish to study in the following year. </p>
	<h3>NCEA Level 1 </h3>
<p>When you have achieved 80 credits at any level, you will qualify for NCEA Level 1 (generally these will be Level 1 credits, but they do not have to be).  Ten of the credits must come from numeracy standards and ten credits must come from literacy standards.  See the literacy and numeracy information in each Year 11 course description. Literacy and numeracy credits do not have to come solely from English, Te Reo Māori or Mathematics courses.  </p>
	<h3>NCEA Level 2 </h3>
<p>NCEA Level 2 requires a minimum of 60 credits at Level 2 or above and up to 20 credits from Level 1.  Credits can be used for more than one qualification, so some of your NCEA Level 1 credits can count toward NCEA Level 2.  Level 1 literacy and numeracy requirements must be met. </p>
	<h3>NCEA Level 3 </h3>
<p>For NCEA Level 3 you will need to achieve 80 credits of which 60 must be at Level 3 or above, and 20 at Level 2 or above.  Level 1 literacy and numeracy requirements must be met. </p>
	<h3>Endorsements </h3>
<p>Your NCEA can be 'endorsed' if your results are good enough.  For example, you could have "NCEA Level 1 with Merit".  Your Record of Achievement will show you if you have been awarded an Endorsement.  If you gain 50 credits at Excellence, your NCEA will be endorsed with Excellence.  Likewise, if you gain 50 credits at Merit (or Merit and Excellence) your NCEA will be endorsed with Merit. </p>
 
<p>You can earn credits counting towards endorsement over more than one year and more than one level. However, they must be gained at the level of the certificate or above.  For example, Level 2 credits will count towards endorsement of a Level 1 NCEA, but Level 1 credits will not count towards endorsement of a Level 2 NCEA. </p>
 
<p>Individual courses can also be ‘endorsed’ if you gain 14 or more credits at Excellence or 14 credits or more at Merit and Excellence in one course, in one academic year.  At least three credits must be from internally assessed standards and at least three credits from externally assessed standards with the exception of Physical Education courses (14 internal credits) and Level 3 Visual Arts courses (either 14 internal or 14 external credits).</p> 
 
	<h3>University entrance </h3> 
<p>University Entrance (UE) will continue to be the minimum requirement to go to a New Zealand university.  To be awarded UE you will need:</p>
	<ul>
		<li>NCEA Level 3 </li>
		<li> Three subjects – at Level 3 or above, made up of: 
			<ul>
				<li> 14 credits each, in three approved subjects </li>
			</ul>
		</li>
		<li>Literacy – 10 credits at Level 2 or above, made up of: 
			<ul>
				<li>5 credits in reading</li>
				<li>5 credits in writing</li>
			</ul>
		</li>
		<li>Numeracy – 10 credits at Level 1 or above, made up of:
			<ul>
			<li>Achievement standards – specified achievement standards available through a range of subjects, or</li>
			<li>Unit standards – package of three numeracy unit standards (26623, 26626, 26627 – all three required)</li>
			</ul>
		</li>
		</ul>
 
<p>Once you have met the requirements for University Entrance this will appear on your Record of Achievement.</p>
 
<p>Most universities now have specific requirements for entry to particular university courses.  If you have a particular university course in mind, check the university entry criteria as soon as possible with the careers advisor.  In particular, the universities based in Wellington, Dunedin, Palmerston North and Auckland have introduced a points system based on NCEA grades from university approved Level 3 subjects.  Merit and Excellence grades generate more points than an Achieved grade, so you are advised to put in the necessary effort to gain the highest possible grades over your best 80 credits from University Entrance approved Level 3 subjects. </p>
<h3>Scholarship </h3>
<p>Scholarship is externally assessed.  It is designed to extend top-level students and to financially assist very able students to attend university.  Students sitting Scholarship will be assessed on their ability to think laterally and in the abstract.  The examinations cover the same content as Level 3 Achievement Standards, but the standard is much higher.  A course of study must be completed.  The extra examinations, which require additional work, can impact on NCEA Level 3 results, but we encourage able students to enrol in Scholarship.  Results are received after the start of the university year and they do not contribute to University Entrance or NCEA Level 3. </p>
	<h3>NCEA Fees and Financial Assistance </h3>
			<p>NCEA fees are invoiced separately from other school fees.</p>
 
<p>The cost for domestic students is currently $76.70 which is a flat fee for any number of credits and a New Zealand Scholarship fee of $30 (GST incl.) per Scholarship subject. The cost for International fee-paying students is currently $383.30 and $102.20 (GST incl.) per Scholarship subject.</p>
			<h3>Financial Assistance </h3>
<p>Financial assistance will continue to be available for those students who meet the financial assistance criteria based on income, benefit or multiple-candidate applications from one family.  Application forms are available on the NZQA website.  Updated details are posted each year in May on the Onslow College website. </p>
 
			<p>International students are not eligible for financial assistance. </p>
 
<p>See the Onslow College website and your green National Assessment Handbook for further details regarding management of NCEA. </p>
			
			<h3>Duplicate Standards </h3>
 
<p>It is possible that courses may offer the same NCEA standard.  This happens mainly in Technology subjects.  Students can only record the credits once.  Any student taking more than one Technology subject needs to talk to all teachers and make a decision about which course will award the credits.  Course endorsement will be a factor in the choice</p>
	
